<?php
// 1. Вывод текста и переменной

// Создаем переменную $name и сохраняем в нее имя "Алексей"
$name = "Алексей";

// Выводим на экран текст "Hello, world!" и значение переменной $name (имя пользователя)
echo "Hello, world! Меня зовут $name.<br>";

// Меняем значение переменной $name на другое имя — "Мария"
$name = "Мария";

// Снова выводим на экран текст "Hello, world!" и текущее значение переменной $name
echo "Hello, world! Меня зовут $name.<br>";


// 2. Сложение чисел и строка с результатом

// Создаем две переменные $a и $b и присваиваем им числа 3 и 5
$a = 3;
$b = 5;

// Считаем сумму этих чисел и сохраняем результат в переменной $sum
$sum = $a + $b;

// Выводим на экран текст с суммой чисел: "Сумма 3 и 5 равна 8"
echo "Сумма $a и $b равна $sum.<br>";


// 3. Условное сообщение

// Создаем переменную $age и присваиваем ей значение 20 (возраст пользователя)
$age = 20;

// Проверяем: если возраст больше или равен 18, выводим "Вы совершеннолетний", иначе — "Вы несовершеннолетний"
if ($age >= 18) {
    echo "Вы совершеннолетний.<br>";
} else {
    echo "Вы несовершеннолетний.<br>";
}


// 4. Проверка длины строки

// Создаем переменную $word и присваиваем ей значение "PHP" (какое-то слово)
$word = "PHP";

// Проверяем, больше ли длина слова $word пяти символов
if (strlen($word) > 5) {
    // Если больше пяти символов, выводим "Длинное слово"
    echo "Длинное слово.<br>";
} else {
    // Иначе выводим "Короткое слово"
    echo "Короткое слово.<br>";
}


// 5. Вывод чисел в строку

// Создаем три переменные $num1, $num2 и $num3 и присваиваем им значения 1, 2 и 3
$num1 = 1;
$num2 = 2;
$num3 = 3;

// Выводим на экран значения переменных, разделенные запятыми, в формате "1, 2, 3"
echo "$num1, $num2, $num3";

